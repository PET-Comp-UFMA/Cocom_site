            <h1>Histórico</h1>
    </div>
    <div class="col-ms-12 text-center">
                <p>O registro histórico dos eventos da informática na UFMA tem sua origem no ano de 1978, quando o Prof. João Fernando Atta de Oliveira Pantoja, promoveu a criação e instalação do que hoje é o Núcleo de Tecnologia da Informação (NTI), não como funciona atualmente, mas sim, de um órgão, que cumprisse as funções de uma Central de Comunicação, Documentação e Informática. Posteriormente, após retornar do seu doutorado na Inglaterra, elaborou um projeto para instalação do Curso de Ciência da Computação na UFMA, em nível de bacharelado. Neste projeto, o Prof. Pantoja contou com a participação de Wagner de Mello Moura, que no início dos anos de 1990 chegou a dirigir o NPD (como o STI se chamava na época).</p>
                <p>Esse projeto não obteve, inicialmente, a compreensão e o apoio dos dirigentes da Universidade naquela época. A decisão política de criar e instalar o curso, foi tomada somente quando o Prof. Pantoja estava afastado para a realização do curso de Pós-Doutorado no exterior.</p>
                <p>Dessa maneira, uma comissão foi formada com o objetivo de analisar o projeto criado pelo Prof. Pantoja e apresentar uma proposta que obtivesse apoio e aprovação da comunidade Universitária. Para a presidência dessa comissão foi indicado o Prof. José Gomes de Matos, do departamento de Engenharia Elétrica.</p>
                <p>Também fizeram parte dessa comissão, os seguintes professores:</p>
                <ul>
                    <li>Prof. José Gomes de Matos – DEENG</li>
                    <li>Prof. José Roberto Quezada Pena - DEENG</li>
                    <li>Prof. Lindberque Cavalcanti Conde - DEENG</li>
                    <li>Prof. Carlos Henrique Santos Sales - DEENG</li>
                    <li>Profa. Maria da Guia da Silva - DEENG</li>
                    <li>Prof. Gualter Gonçalves Lopes – DEMAT</li>
                    <li>Prof. Ivar Ribeiro Hortegal – DEMAT</li>
                    <li>Prof. Raimundo Renato Patrício – DEMAT</li>
                </ul>
                <p>Dada a pouca experiência da maioria dos membros da comissão nos assuntos de Informática foi, então, chamado para participar do processo Jorge Henrique Marques Caracas, que era tido como referência na área no estado. Ele estudou o projeto do Prof. Pantoja e reconheceu como de altíssima qualidade. Mesmo assim, sentiu a necessidade de agregar a experiência de outros especialistas ao trabalho inicial, como forma de ampliar a discussão. Assim, o Prof. Diógenes de Carvalho Aquino, que na época era do corpo docente da UEMA, foi incluído ao grupo.</p>
                <p>Reunidos os elementos técnicos suficientes e acrescidos da firme determinação do Prof. Patrício, tinha-se, finalmente, o curso de Ciência da Computação incluído no elenco de cursos da UFMA. Foi a Resolução No. 48/87 – CONSUN que autorizou o seu funcionamento e, por consequência, provocou a criação, no âmbito da UFMA, da Coordenação do Curso de Ciência da Computação vinculada ao Departamento de Matemática do Centro Tecnológico (Resolução CONSUN No. 48/87 de 30.11.1987).</p>
                <p>O curso começou suas atividades a partir do segundo semestre de 1987 com 20 alunos e compreendia um total mínimo de 3.915 horas e 226 créditos integralizáveis, sendo que seu tempo de conclusão era de, no mínimo, nove semestres e, no máximo, dezesseis semestres letivos.</p>
                <p>Para possibilitar o seu funcionamento inicial, foi nomeado como Coordenador de Implantação o Prof. Patrício, para dar início às primeiras providências a fim de viabilizar o andamento do curso, quando de suas instalações provisórias no prédio do antigo CEB. Ele também foi o responsável pela implantação os primeiros laboratórios do curso, formados de computadores Cobra, onde linguagens de programação como Fortran IV, Cobol e Assembly eram lecionadas, além dos sistemas operacionais SOM, SOD, SPM e SOX.</p>
                <p>A Coordenação do curso continuou com o Prof. Patrício por mais alguns anos, sendo substituído pelo Prof. Caracas e, posteriormente, pelo Prof. Diógenes, que assumiu por indicação do Magnífico Reitor, a pedido do Colegiado.</p>
                <p>Foi por meio da Resolução CONSUN No. 10/92 de 30/09/1992 que o Departamento de Informática foi criado, por desmembramento do Departamento de Matemática que abrigava até esta data os professores da área. Coube ao Prof. Patrício, especialista em assumir desafios, a árdua tarefa de instalar e consolidar o Departamento de Informática.</p>
                <p>O ato de reconhecimento do curso se deu pela Portaria No. 1524/93-MEC, de 21.10.1993 (D.O.U. de 22/10/1993) – Parecer No. 534/93 – CFE. A partir daquele momento, o curso carregava consigo o título de Bacharelado. Devido a greve ocorrida no segundo semestre do ano de 1992, a primeira turma de formandos só colocou grau em 16 abril de 1993, tendo como concluintes:</p>
                <ul>
                    <li>Alexandre César Muniz de Oliveira</li>
                    <li>Kércio Augusto Sekeff Sallem</li>
                    <li>Mário Antônio Meireles Teixeira</li>
                    <li>Paulo Sérgio Raposo Nascimento</li>
                </ul>                
                <p>Decorridos quase 35 anos de sua fundação, o curso de Ciência da Computação evoluiu e hoje conta com 4 laboratórios dedicados, exclusivamente, às atividades de ensino e 10 laboratórios de pesquisa (LABJEDI, LABMINT, LACMOR, LSDI, LAWS, LIDI, MECANET, NCA, Telemídia MA e VIPLAB), uma infraestrutura que atende, atualmente, cerca de 120 alunos distribuídos entre alunos de graduação, mestrado e doutorado. Além de contar com um Programa de Ensino Tutorial (PET), que atualmente possui 18 alunos, incluindo bolsistas e voluntários, participando ativamente.</p>
                <p>O curso tem, ainda, o Programa de Pós-Graduação em Ciência da Computação (PPGCC), que oferta do mestrado em nível acadêmico e o Doutorado em Ciência da Computação em Associação UFMA/UFPI.</p>
